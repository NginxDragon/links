    <script>
$(document).ready(function(){
  $("#carihalaman").keydown(function(){
    $("#carihalaman").css("background-color", "white");
    $("#livesearch").addClass("hasilcari");
    $("#livesearch").show();
  });

  $("#carihalaman").keyup(function(){
    $("#carihalaman").css("background-color", "white");
  });

});
</script>


<script>
$(document).ready(function(){
  $("#livesearch").on({
    mouseenter: function(){
      $(this).css("background-color", "white");
      $("#livesearch").addClass("hasilcari");
    },  

    mouseleave: function(){
      $(this).css("background-color", "white");
      $("#livesearch").removeClass("hasilcari");
      $("#carihalaman").val(''); 
      $("#livesearch").empty();
      $("#livesearch").hide();
    }, 

    click: function(){
      $("#livesearch").removeClass("hasilcari");
      $("#carihalaman").val('');   
      $("#livesearch").empty();
      $("#livesearch").hide();
    }  
  });
});
</script>
